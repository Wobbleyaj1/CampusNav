# List module

class List:
    def __init__(self):
        """Initialize the list."""

    def add(self, item):
        """Add an item to the list."""

    def remove(self, item):
        """Remove an item from the list."""

    def get(self, index):
        """Retrieve an item by its index."""

    def contains(self, item):
        """Check if the list contains an item."""

    def size(self):
        """Return the number of items in the list."""

    def display(self):
        """Display the list.""" 